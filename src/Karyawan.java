public class Karyawan {
    String nama, alamat, tlp;
    public Karyawan(String nama, String alamat) {
        this.nama = nama;
        this.alamat = alamat;
    }
    public Karyawan(String nama, String alamat, String tlp){
        this.nama = nama;
        this.alamat = alamat;
        this.tlp = tlp;
    }
}
